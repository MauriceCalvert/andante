"""Episode kernel source — sequential episode leader voice generation.

Extracts short motivic kernels from fugue subject material and chains
them into ascending or descending sequences that fill a target duration
and land on a specified degree. Produces the leader voice for sequential
episodes; the companion voice is generated by Viterbi in phrase_writer.
"""
import logging
import random
from fractions import Fraction

from builder.types import Note as BuilderNote
from motifs.fragen import Kernel, Note, extract_kernels, sequence_kernels
from motifs.head_generator import degrees_to_midi
from motifs.subject_loader import SubjectTriple
from shared.key import Key
from shared.music_math import parse_metre
from shared.voice_types import Range

_log: logging.Logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

_KERNELS_PER_FAMILY: int = 3
_MAX_CANDIDATES: int = 200
_MAX_KERNEL_DURATION: Fraction = Fraction(1, 2)
_MAX_NODES: int = 50000
_MAX_JUNCTION_DEGREES: int = 5  # max diatonic interval at segment boundaries
_MAX_SEGMENTS: int = 5
_SEED: int = 42
_DEGREE_SEARCH_LO: int = -14
_DEGREE_SEARCH_HI: int = 22
_DISTANCE_CANDIDATES: dict[int, tuple[int, ...]] = {
    1: (3, 2, 4, 1),
    2: (5, 4, 6, 3, 7),
    3: (7, 6, 5, 4),
}
_DISTANCE_DEFAULT: tuple[int, ...] = (5, 4, 6, 3, 7, 2)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _closure_ok(segments: list[tuple[Kernel, int]]) -> bool:
    """Last 2 note durations must be >= the preceding 2."""
    durations: list[Fraction] = []
    for k, reps in segments:
        for _ in range(reps):
            durations.extend(k.durations)
    if len(durations) < 4:
        return True
    return (
        durations[-2] >= durations[-4]
        and durations[-1] >= durations[-3]
    )


def _junctions_ok(segments: list[tuple[Kernel, int]], step: int) -> bool:
    """All inter-iteration junctions within _MAX_JUNCTION_DEGREES."""
    for i, (k, reps) in enumerate(segments):
        has_successor: bool = i < len(segments) - 1 or reps > 1
        if has_successor:
            junction: int = step - k.degrees[-1]
            if abs(junction) > _MAX_JUNCTION_DEGREES:
                return False
    return True


def _build_pool(kernels: list[Kernel]) -> list[Kernel]:
    """Keep up to _KERNELS_PER_FAMILY distinct kernels per source family."""
    families: dict[str, list[Kernel]] = {}
    for k in kernels:
        if k.total_duration > _MAX_KERNEL_DURATION:
            continue
        fam: str = k.source.removesuffix("_inv")
        families.setdefault(fam, []).append(k)
    pool: list[Kernel] = []
    for fam in sorted(families.keys()):
        candidates: list[Kernel] = families[fam]
        candidates.sort(key=lambda k: (len(k.degrees), k.total_duration))
        seen_lengths: set[int] = set()
        selected: list[Kernel] = []
        for c in candidates:
            n: int = len(c.degrees)
            if n not in seen_lengths:
                selected.append(c)
                seen_lengths.add(n)
            if len(selected) >= _KERNELS_PER_FAMILY:
                break
        if len(selected) < _KERNELS_PER_FAMILY:
            for c in candidates:
                if c not in selected:
                    selected.append(c)
                if len(selected) >= _KERNELS_PER_FAMILY:
                    break
        pool.extend(selected)
    return pool


def _midi_to_nearest_degree(
    midi: int,
    tonic_midi: int,
    mode: str,
) -> int:
    """Find the scale degree whose MIDI pitch is closest to target."""
    best_deg: int = 1
    best_dist: int = 999
    for deg in range(_DEGREE_SEARCH_LO, _DEGREE_SEARCH_HI):
        m: int = degrees_to_midi((deg,), tonic_midi, mode)[0]
        dist: int = abs(m - midi)
        if dist < best_dist:
            best_dist = dist
            best_deg = deg
    return best_deg


