def _to_builder_notes(
    notes: list[Note],
    tonic_midi: int,
    mode: str,
    track: int,
    start_offset: Fraction,
    target_range: Range,
) -> tuple[BuilderNote, ...] | None:
    """Convert degree-space notes to builder Notes, octave-shifted into range."""
    midi_pitches: list[int] = [
        degrees_to_midi((n.degree,), tonic_midi, mode)[0]
        for n in notes
    ]
    if not midi_pitches:
        return None
    # Octave-shift into target range.
    # The valid shift window is [range.low - lowest, range.high - highest].
    # Find the multiple of 12 nearest the centre of that window.
    lowest: int = min(midi_pitches)
    highest: int = max(midi_pitches)
    span: int = highest - lowest
    range_span: int = target_range.high - target_range.low
    if span > range_span:
        _log.warning(
            "Episode kernel span %d st exceeds range span %d st "
            "[%d, %d]; cannot fit",
            span, range_span, target_range.low, target_range.high,
        )
        return None
    shift_lo: int = target_range.low - lowest
    shift_hi: int = target_range.high - highest
    # Find best multiple of 12 in [shift_lo, shift_hi]
    first_candidate: int = ((shift_lo + 11) // 12) * 12  # ceil to next mult of 12
    if shift_lo <= 0 <= shift_hi:
        first_candidate = 0  # prefer no shift
    else:
        first_candidate = ((shift_lo + 11) // 12) * 12 if shift_lo > 0 else (shift_lo // 12) * 12
    shift: int | None = None
    for candidate in range(first_candidate, shift_hi + 1, 12):
        if shift_lo <= candidate <= shift_hi:
            shift = candidate
            break
    if shift is None:
        # No exact octave shift works — try centring within range
        centre_shift: int = (shift_lo + shift_hi) // 2
        nearest_octave: int = round(centre_shift / 12) * 12
        if shift_lo <= nearest_octave <= shift_hi:
            shift = nearest_octave
    if shift is None:
        _log.warning(
            "Episode kernel cannot fit in range [%d, %d]: "
            "span %d-%d, valid shift window [%d, %d] has no multiple of 12",
            target_range.low, target_range.high, lowest, highest, shift_lo, shift_hi,
        )
        return None
    result: list[BuilderNote] = []
    for note, midi in zip(notes, midi_pitches):
        result.append(BuilderNote(
            offset=note.offset + start_offset,
            pitch=midi + shift,
            duration=note.duration,
            voice=track,
            creator="episode_kernel",
        ))
    return tuple(result)
